use Mojo::File;

our $VERSION = "0.01";

sub save($self) {

    eval {
        my $filepath = $self->context->{context}->{sql_target_dir};
        my $files = $self->context->{context}->{sql};
        my $length = scalar @{$files};
        for (my $i = 0; $i < $length; $i++) {
            my $file = $filepath . @{$files}[$i]->{file} . $self->activity_data->{file}->{filetype};
            open(my $fh, ">", $file);
            print $fh @{$files}[$i]->{data};
            close $fh;
        }
    };
    $self->error->add_error($@) if defined $@ and length($@) > 0;

    return;
}

1;
